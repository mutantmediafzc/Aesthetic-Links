<script type="text/javascript">
  function inputId(tag) {
    return 'tag-checkbox-' + tag;
  }
  function setupTags(tags) {
    document.getElementById('tagging-container').style.display = tags && tags.length ? undefined : 'none';
    var cols = Array.from(document.querySelectorAll('.tagging-container-col'));
    // Setup the DOM
    tags.forEach(function(def, i) {
      var tag = def[0];
      var title = def[1];
      var col = cols[i % cols.length];
      var elt = document.createElement('div');
      col.appendChild(elt);
      var label = document.createElement('label');
      label.classList.add('wonderpush-category-label');
      label.setAttribute('title', title);
      elt.appendChild(label);
      var titleSpan = document.createElement('span');
      titleSpan.classList.add('wonderpush-category-label-text');
      titleSpan.textContent = title;
      label.appendChild(titleSpan);
      var input = document.createElement('input');
      label.appendChild(input);
      input.classList.add('wonderpush-category-label-input')
      input.setAttribute('type', 'checkbox');
      input.setAttribute('value', tag);
      input.setAttribute('id', inputId(tag));
      input.onchange = function() {
        if (input.checked) {
          WonderPushPopupSDK.addTag(tag);
          WonderPushPopupSDK.trackClick(title);
        }
        else WonderPushPopupSDK.removeTag(tag);
      };
      var checkmark = document.createElement('span');
      label.appendChild(checkmark);
      checkmark.classList.add('wonderpush-checkmark');
      var clear = document.createElement('div');
      clear.style.clear = 'both';
      elt.appendChild(clear);
    });
    // Fetch user tags
    WonderPushPopupSDK.getTags()
      .then((userTags) => {
        userTags.forEach(function(userTag) {
          var input = document.getElementById(inputId(userTag));
          if (input) input.setAttribute('checked', true);
        });
      });
  }
</script>

